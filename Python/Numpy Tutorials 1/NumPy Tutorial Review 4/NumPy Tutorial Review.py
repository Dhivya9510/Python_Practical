# Why use NumPy?

  # In Python, we have list that serves the purpose of arrays, but it is slow to process.
  # NumPy array is 50X more faster when comparing the traditional python Lists. 
  # The array object in NumPy is called ndarray, that provides a lot of supporting funtions which makes work more easy. 
  # Arrays are frequently used in Data science, where speed and resources are very much important. 

# What is Data Science?
  # Data Science is a branch of computer science where we study how to store, use and analyze data for deriving information from it.

# Why is NumPy Faster Than Lists?
 # NUMPY ARRAYS ARE STORED AT ONE CONTINUOUS PLACE IN MEMORY UNLIKE LISTS, HENCE WE CAN ACCESS IT QUICKLY AND MANIPULATE THEM VERY EFFICIENTLY
 # This behaviour is called  LOCALITY OF REFERENCE  in computer science. 
 # This is the main reason why NumPy is faster than Lists. 

